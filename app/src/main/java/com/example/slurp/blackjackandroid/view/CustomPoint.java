package com.example.slurp.blackjackandroid.view;

public class CustomPoint {
    private float x;
    private float y;

    public CustomPoint(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
